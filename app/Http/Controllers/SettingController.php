        if ($request->session()->has('idiomas')) {
            $existe = 1;
        }else{
            $request->session()->put('idiomas', 1);
            $existe = 0;
        }
        $session = session('idiomas');

        $idioma = Ml_dashboard::where('many_lenguages_id',$session)->first();
        $idiomas = ManyLenguages::all();
       
        
        return view('admin.setting.index', [
          
            'idioma'     => $idioma,
            'idiomas'    => $idiomas
        ]);
    }

